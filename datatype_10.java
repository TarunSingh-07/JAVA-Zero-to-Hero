package com.company;

public class datatype_10 {
    public static void main(String[] args) {
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;
        System.out.println(b);

//        Increment and Decrement Operators
        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int t = 7;
        System.out.println(t++*5);
        System.out.println(++t*5);
        char ch = 'a';
        System.out.println(++ch);
    }
}
